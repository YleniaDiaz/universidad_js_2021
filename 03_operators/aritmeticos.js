let x = 27;
let y = 10;

//suma
let z = x + y;
console.log('Suma = ' + z);

//resta
z = x - y;
console.log('Resta = ' + z);

//multiplicación
z = x * y;
console.log('Multiplicación = ' + z);

//división
z = x / y;
console.log('División = ' + z);

//módulo (resto de la división)
z = x % y;
console.log('Módulo = ' + z);

//exponente
z = x ** y; //27 elevado a 10
console.log('Exponente = ' + z);